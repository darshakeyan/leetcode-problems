
public class Problem2 {
    public static void main(String[] args) {
      int rows = 5;
      System.out.println("Hello World");
      for (int i=0; i<=rows; i++) {
        for (int j=1; j<=i; j++) {
            System.out.print(j + "  ");
        }
        System.out.println();
      }
    }
    
}